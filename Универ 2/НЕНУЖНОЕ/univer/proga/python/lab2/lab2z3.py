n = input('введите числоb ')

i = 0;

while i < 3:
    l = n[i]
    l = int(l)

    if l == 0:
        print('ноль')
    elif l == 1:
        print('один')
    elif l == 2:
        print('два')
    elif l == 3:
        print('три')
    elif l == 4:
        print('четыре')
    elif l == 5:
        print('пять')
    elif l == 6:
        print('шесть')
    elif l == 7:
        print('семь')
    elif l == 8:
        print('восемь')
    elif l == 9:
        print('девять')
    i = i + 1;
